package net.skhu.dto;

public class CoreSubject {
	int id;
	int yearOfClass;
	String yearOfSemester;
	String subjectId;
	String completeType;
	String grade;
	String name;
	int subjectScore;
	String coreCode;
	String coreDetailName;
	String coreName;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getYearOfClass() {
		return yearOfClass;
	}
	public void setYearOfClass(int yearOfClass) {
		this.yearOfClass = yearOfClass;
	}
	public String getYearOfSemester() {
		return yearOfSemester;
	}
	public void setYearOfSemester(String yearOfSemester) {
		this.yearOfSemester = yearOfSemester;
	}
	public String getSubjectId() {
		return subjectId;
	}
	public void setSubjectId(String subjectId) {
		this.subjectId = subjectId;
	}
	public String getCompleteType() {
		return completeType;
	}
	public void setCompleteType(String completeType) {
		this.completeType = completeType;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getSubjectScore() {
		return subjectScore;
	}
	public void setSubjectScore(int subjectScore) {
		this.subjectScore = subjectScore;
	}
	public String getCoreCode() {
		return coreCode;
	}
	public void setCoreCode(String coreCode) {
		this.coreCode = coreCode;
	}
	public String getCoreDetailName() {
		return coreDetailName;
	}
	public void setCoreDetailName(String coreDetailName) {
		this.coreDetailName = coreDetailName;
	}
	
	public String getCoreName() {
		return coreName;
	}
	public void setCoreName(String coreName) {
		this.coreName = coreName;
	}
}
