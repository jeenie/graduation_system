package net.skhu.dto;

public class OtherMajor {
	int studentId;
	String anotherMajorId;
	String otherMajorName;
	int score;
	int year;
	int semester;
	String type;
	int departmentId;
	String departmentName;

	public int getStudentId() {
		return studentId;
	}
	public void setStudentId(int studentId) {
		this.studentId = studentId;
	}
	public String getAnotherMajorId() {
		return anotherMajorId;
	}
	public void setAnotherMajorId(String anotherMajorId) {
		this.anotherMajorId = anotherMajorId;
	}
	public String getOtherMajorName() {
		return otherMajorName;
	}
	public void setOtherMajorName(String otherMajorName) {
		this.otherMajorName = otherMajorName;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getSemester() {
		return semester;
	}
	public void setSemester(int semester) {
		this.semester = semester;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getDepartmentId() {
		return departmentId;
	}
	public void setDepartmentId(int departmentId) {
		this.departmentId = departmentId;
	}
	public String getDepartmentName() {
		return departmentName;
	}
	public void setDepartmentName(String departmentName) {
		this.departmentName = departmentName;
	}
}
